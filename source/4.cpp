#include<iostream>
using namespace std;

int main() {
	int num1 = 5;
	int num2 = 10;

	cout << "num1 + num2 = " << num1 + num2 << "\n";
	cout << "num2 - num1 = " << num2 - num1 << "\n";
	cout << "num1 * num2 = " << num1 * num2 << "\n";
	cout << "num2 / num1 = " << num2 / num1 << "\n";
	cout << "num2 % num1 = " << num2 % num1 << "\n";

	return 0;
}
